 import java.util.Scanner;
 
public class Large {
    public static void main(String[] args) {

      Scanner in = new Scanner(System.in);
       
       int a = in.nextInt();
       int b = in.nextInt();

       if(a > b){
        System.out.println(a + " is large number");
       }else{
        System.out.println(b + " is large number");
       }

  }
}
     
 

 
